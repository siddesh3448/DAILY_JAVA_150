public class print_2_ka_table {

    static void print2katable(){
        for(int i=1;i<=10;i++){
            int ans = 2*i;
            System.out.println("--> "+ans);
        }
    }
    public static void main(String[] args) {
        System.out.println("~~ Start ~~");
        print2katable();
        System.out.println("~~ End ~~");
    }
}
