public class Assignment_Operators {
    public static void main(String[] args) {
        int a = 20;
        System.out.println("After += : "+(a += 10));
        System.out.println("After -= : "+(a -= 5));
        System.out.println("After *= : "+(a *= 2));
        System.out.println("After /= : "+(a /= 5));
        System.out.println("After %= : "+(a %= 3));
    }
}
