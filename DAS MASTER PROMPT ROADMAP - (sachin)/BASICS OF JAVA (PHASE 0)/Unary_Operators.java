public class Unary_Operators {
    public static void main(String[] args) {
        int a = 25;
        System.out.println("Original =  "+(a));
        System.out.println("Unary Plus =  "+(+a));
        System.out.println("Unary Minus =  "+(-a));
        System.out.println("Original Again =  "+(a));
    }
}
